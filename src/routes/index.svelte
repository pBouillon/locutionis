<section>
  <h2 class="subtitle">
    Objectif
  </h2>

  <p class="description">
    Le but de <em>Locutionis</em> est de permettre à n'importe quel curieux·euse
    de parcourir et/ou découvrir des figures de style de la langue française.
  </p>

  <p class="description">
    <em>Épanorthoses</em>, <em>antanaclases</em> et autres <em>antonomases</em>
    n'auront plus de secrets pour vous !
  </p>

  <p class="description">
    N'étant qu'hobyiste, les informations ici sont majoritairement agrégées
    depuis <a href="/sources" class="link">diverses ressources</a>. Pensez-donc
    à vérifier les informations mentionnées !
  </p>
</section>

<section>
  <h2 class="subtitle">
    Pourquoi Locutionis
  </h2>

  <p class="description">
    Amateur des figures de style que j'ai pu découvrir au cours des années, je
    me suis toujours amusé tant de leur phonétique que de leur capacité à
    qualifier la structure de nos phrases.
  </p>

  <p class="description">
    Ayant des difficultés à les garder en mémoire, j'ai d'abord opté pour noter
    mes découvertes sur un carnet, avant d'abandonner à cause de son caractère
    peu pratique et mon manque de rigueur.
  </p>

  <p class="description">
    C'est finalement en souhaitant mêler ma passion pour ces dernières et pour
    l'informatique que j'ai choisi d'en faire un site accessible à tous, en
    espérant aussi naïvement qu'un jour cela puisse aider quelqu'un !
  </p>
</section>

